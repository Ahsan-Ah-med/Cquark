$(function() {
	// (Optional) Active an item if it has the class "is-active"	
	$(".accordion > .accordion-item.is-active").children(".accordion-panel").slideDown();
	
	$(".accordion > .accordion-item").click(function() {
		// Cancel the siblings
		$(this).siblings(".accordion-item").removeClass("is-active").children(".accordion-panel").slideUp();
		// Toggle the item
		$(this).toggleClass("is-active").children(".accordion-panel").slideToggle("ease-out");
	});
});


document.addEventListener('DOMContentLoaded', function() {
    function moveProductDifference() {
        var productDifference = document.querySelector('.heading-wrapper--recommended-products');
        var relatedProductsWrapper = document.querySelector('.trustpilot-widget-trustbox-1-wrapper');
        relatedProductsWrapper.parentNode.insertBefore(productDifference, relatedProductsWrapper.nextSibling);
    }
    moveProductDifference();
    window.addEventListener('page:load', moveProductDifference);
    window.addEventListener('page:change', moveProductDifference);
});

